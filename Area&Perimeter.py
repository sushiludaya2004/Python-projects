#area of rectangle is <area> and perimeter is <perimeter>


breadth = float(input("What is the breadth: "))
length = float(input("What is the length: "))

Area = length * breadth
Perimeter = 2*(length + breadth)

print("Area of rectangle is " + str(Area) + " and perimeter is " + str(Perimeter))


               
